class UserData {
  final String userId;
  final String firstName;
  final String lastName;
  final int number;
  final String email;
  final String role;
  final String pic;

  UserData({
    required this.userId,
    required this.pic,
    required this.firstName,
    required this.lastName,
    required this.number,
    required this.email,
    required this.role,
  });
}
